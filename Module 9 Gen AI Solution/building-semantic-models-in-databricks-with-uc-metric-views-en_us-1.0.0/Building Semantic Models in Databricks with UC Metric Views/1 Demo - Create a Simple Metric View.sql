-- MAGIC ## C. Create a Metric View Using the Databricks UI
-- MAGIC
-- MAGIC ### C1. Define the Metric View's Basic Details
-- MAGIC
-- MAGIC 1. Right click on **Catalog** in the main navigation bar and select **Open Link in New Tab** to open **Catalog Explorer**.
-- MAGIC
-- MAGIC 1. In **Catalog Explorer**, navigate to **samples.tpch.lineitem**, the table to use as the fact table for the metric view. 
-- MAGIC
-- MAGIC 1. Select the **Create** button at top right and select **Metric View**. 
-- MAGIC
-- MAGIC 1. Use the dropdowns for catalog and schema at the top left of the panel to place the metric view you are building in the catalog with name beginning with `labuser` and the **default** schema .
-- MAGIC
-- MAGIC 1. If the user interface brings up the Databricks Assistant chat window, close it for now.
-- MAGIC
-- MAGIC 1. Click on the proposed name of the metric view, which contains the name of the base table, and change it to **order_details**.
-- MAGIC
-- MAGIC 1. You will see a skeletal metric view YAML file in the editor window, consisting of these sections:
-- MAGIC
-- MAGIC | Section | Meaning |
-- MAGIC |----|----|
-- MAGIC | **version** | Databricks's version of the metric-view specification |
-- MAGIC | **source** | the fact table for this metric view |
-- MAGIC
-- MAGIC **CAUTION**: If the user interface proposed sections for you other than these two, delete them for now. 
-- MAGIC
-- MAGIC The first line of the skeleton metric view should read
-- MAGIC
-- MAGIC ```
-- MAGIC version: 1.1
-- MAGIC ```
-- MAGIC
-- MAGIC If not, change the line so that it does.
-- MAGIC
-- MAGIC **NOTE**: The lab exercises in this course were designed to work with version 1.1 of the metric views API. If the sample metric view proposed for you by the user interface starts with a different number, Databricks has introduced a new version of the API since this course was released. However, specifying version 1.1 in your metric views in this course will ensure that the lab exercises continue to work as intended. 
-- MAGIC
-- MAGIC 8. The next line identifies the fact table you chose to build this metric view on:
-- MAGIC
-- MAGIC ```
-- MAGIC source: samples.tpch.lineitem
-- MAGIC ```
-- MAGIC
-- MAGIC 9. If the UI proposed a number of sample dimensions and measures for you, delete them for now. Delete all the suggested measures by highlighting them and cutting.
-- MAGIC
-- MAGIC 10. Add a **joins** section with joins that bring in enriching data for the fact table. Insert the following below **source**:
-- MAGIC
-- MAGIC ```
-- MAGIC joins:
-- MAGIC    - name: orders
-- MAGIC      source: samples.tpch.orders
-- MAGIC      on: orders.o_orderkey = source.l_orderkey
-- MAGIC      joins:
-- MAGIC      - name: customers
-- MAGIC        source: samples.tpch.customer
-- MAGIC        on: orders.o_custkey = customers.c_custkey
-- MAGIC ```
-- MAGIC
-- MAGIC Make sure to get the indentation exactly as shown. Use spaces, not tabs.
-- MAGIC
-- MAGIC **BEST PRACTICE**: Always look for opportunities to focus the rows returned by a metric view on what your audience of data consumers needs. Don't count on the use of WHERE clauses when the metric view is queried. Instead, proactively limit what is returned to what your business needs using the `filter` clause.
-- MAGIC
-- MAGIC 11. Add a filter, so that we can reduce the cost of this metric view by limiting the number of rows it will return. Insert this line, without any indentation, just below the source section, above the **joins** keyword.
-- MAGIC
-- MAGIC ```
-- MAGIC filter: YEAR(orders.o_orderdate) > 1994
-- MAGIC ```
-- MAGIC
-- MAGIC 12. Now add some measures. Remember that a measure is an indicator of business activity, in terms our data consumers understand, independently of how that indicator is grouped or sliced. Insert a `measures:` keyword, on a new line at the end of the file, so that it comprises a new  **measures** section, and then some measure definitions:
-- MAGIC
-- MAGIC ```
-- MAGIC measures:
-- MAGIC   - name: total_item_count
-- MAGIC     expr: COUNT(source.l_orderkey)
-- MAGIC     display_name: Total Item Count
-- MAGIC   - name: total_distinct_orders
-- MAGIC     expr: COUNT(DISTINCT source.l_orderkey)
-- MAGIC     display_name: Total Distinct Orders
-- MAGIC ```
-- MAGIC
-- MAGIC 13. Now add some dimensions. Remember that a dimension is a way to slice or group the offered measures; the selection of dimensions is driven by the needs of the business. For example, if your data consumers make decisions based on quarterly data, you would choose to offer dimensions based on quarters. Insert a `dimensions:` keyword, on a new line at the end of the file, so that it comprises a new  **dimensions** section, and then some measure definitions:
-- MAGIC
-- MAGIC ```
-- MAGIC dimensions:
-- MAGIC   - name: order_ID
-- MAGIC     expr: orders.o_orderkey
-- MAGIC     display_name: Order ID
-- MAGIC   - name: order_date
-- MAGIC     expr: orders.o_orderdate
-- MAGIC     display_name: Order Date
-- MAGIC   - name: shipping_mode
-- MAGIC     expr: source.l_shipmode
-- MAGIC     display_name: Shipping Mode
-- MAGIC
-- MAGIC ```
-- MAGIC
-- MAGIC **BEST PRACTICE**: Name your dimensions and measures to make them clear and meaningful for your data consumers, NOT for the data and AI team. Assume that dimension and measure names will be seen by non-technical business data consumers who do not know or care about how your Databricks estate is implemented. 
-- MAGIC
-- MAGIC **BEST PRACTICE**: For each measure and dimension, supply a **name** that is easy to work with in code and expression contexts (for example, not including spaces or parentheses), and also a **display_name** that is ideal for use in dashboards or other presentation modes.
-- MAGIC
-- MAGIC **NOTE**: There is no requirement for measure and dimension display names to contain spaces, although it's a common practice, in order to make the names consumer-oriented. Their actual names can also contain spaces, although quoting with backticks becomes required in SQL contexts if they do. 
-- MAGIC
-- MAGIC 14. Now click **Save** at top right. If the UI points out any syntax errors, correct them and try again.
-- MAGIC
-- MAGIC 15. When prompted, click **Keep editing** to refine the metric view further.


-- MAGIC ## E. (OPTIONAL) Create a metric view using the SQL Editor
-- MAGIC
-- MAGIC It is also possible to make a metric view completely within the SQL Editor, using SQL Data Definition Language.
-- MAGIC
-- MAGIC 1. In the left-hand navigation of the Databricks UI, click on SQL Editor. 
-- MAGIC
-- MAGIC 1. In the top bar of the SQL Editor, choose your assigned catalog (starting with `labuser`) and the `default` schema.
-- MAGIC
-- MAGIC 1. Continue entering code into the SQL Editor window:
-- MAGIC
-- MAGIC ```
-- MAGIC CREATE OR REPLACE VIEW order_details2
-- MAGIC (
-- MAGIC  order_ID,
-- MAGIC  order_date,
-- MAGIC  shipping_mode,
-- MAGIC  ship_date,
-- MAGIC  total_item_count,
-- MAGIC  total_distinct_orders
-- MAGIC )
-- MAGIC ```
-- MAGIC
-- MAGIC 4. Now append the SQL below to your query being composed (*don't hit Run yet*).
-- MAGIC
-- MAGIC ```
-- MAGIC WITH METRICS
-- MAGIC LANGUAGE YAML
-- MAGIC AS $$
-- MAGIC ```
-- MAGIC
-- MAGIC That $$ tells SQL to expect an embedded document. 
-- MAGIC
-- MAGIC 5. Now paste the YAML file from your other window (or from your clipboard if it's still there) immediately below the $$.  *Don't hit Run yet*.
-- MAGIC
-- MAGIC 6. Finally, immediately below what you pasted in insert this:
-- MAGIC ```
-- MAGIC $$ ;
-- MAGIC ```
-- MAGIC
-- MAGIC This second $$ ends the embedded document, and the semicolon is the regular syntax for ending a SQL query.
-- MAGIC
-- MAGIC 7. Now **Run** the query.
-- MAGIC
-- MAGIC 8. Using **Catalog** in the left hand navigation, drill into the **labuserXXX** catalog and the **default** schema. You will see that you now have two metric views defined.


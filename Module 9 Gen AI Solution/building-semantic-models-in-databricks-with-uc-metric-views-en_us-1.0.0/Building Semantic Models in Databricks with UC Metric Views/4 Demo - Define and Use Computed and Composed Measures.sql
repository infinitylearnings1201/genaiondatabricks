-- MAGIC ## B4. Define Computed and Composed Measures
-- MAGIC
-- MAGIC Metric views provide a focal point for data professionals to drive auditing and agreement. By defining complex measures here, in the metric view, we can make it easy for the whole business to adopt those definitions.
-- MAGIC
-- MAGIC 1. Add this to the end of the **measures** section (taking care to indent properly):
-- MAGIC
-- MAGIC ```
-- MAGIC   - name: revenue_less_discounts
-- MAGIC     expr: SUM(source.l_extendedprice * (1 - source.l_discount))
-- MAGIC     display_name: Total Revenue Less Discounts ($)
-- MAGIC
-- MAGIC ```
-- MAGIC
-- MAGIC If everyone agrees to use this measure as the basis for all profit computations, no one will ever forget to subtract the discounts.
-- MAGIC
-- MAGIC 2. Add this immediately below the previous measure (taking care to indent properly):
-- MAGIC
-- MAGIC ```
-- MAGIC   - name: average_discount_pct
-- MAGIC     expr: AVG(source.l_discount)
-- MAGIC     display_name: Average Discount (%)
-- MAGIC     format:
-- MAGIC       type: percentage
-- MAGIC       decimal_places: 
-- MAGIC         type: exact
-- MAGIC         places: 2 
-- MAGIC ```
-- MAGIC
-- MAGIC Now discount percentages will displayed to the nearest 2 decimal places in contexts downstream from this metric view.
-- MAGIC
-- MAGIC 3. Here is a technique you can use to count the occurrences of phenomena in your data and compute percentages. Let's say you want a metric that shows the rate at which items were returned. Returned items are marked with a value of **R** in the `l_returnflag` field. It's easy to count all records, with the familiar SQL expression `COUNT(*)`, but how can we count only those records marked as returned?
-- MAGIC
-- MAGIC Add this to the end of your `measures` section (taking care to indent properly).
-- MAGIC
-- MAGIC
-- MAGIC ```
-- MAGIC   - name: item_return_rate_pct
-- MAGIC     expr: |
-- MAGIC           SUM(CASE WHEN source.l_returnflag = 'R' 
-- MAGIC                    THEN 1
-- MAGIC                    ELSE 0
-- MAGIC               END) 
-- MAGIC           / COUNT(*)
-- MAGIC     display_name: Item Return Rate (%)
-- MAGIC     format:
-- MAGIC       type: percentage
-- MAGIC       decimal_places: 
-- MAGIC         type: exact
-- MAGIC         places: 2
-- MAGIC
-- MAGIC ```
-- MAGIC
-- MAGIC 4. Save the definition of the metric view.
-- MAGIC

-- MAGIC ## B3. Create a Composed Dimension
-- MAGIC
-- MAGIC Defining dimensions in terms of others makes your logic clearer and easier to maintain. Suppose we wanted to create a dimension for how long it is taking us to ship each order. The business doesn't care about the exact number of days, but rather whether the range meets policy. Shipments between 0 and 2 days after receipt of the order are policy-compliant, while shipments between 2 and 4 days are exceptions, and anything longer is a serious problem we want to address.
-- MAGIC
-- MAGIC The naive way to do that would be this:
-- MAGIC
-- MAGIC ```
-- MAGIC   - name: order_delay_bucket
-- MAGIC     comment: Too much repeated code; also risks introducing nulls
-- MAGIC     expr: |
-- MAGIC           CASE
-- MAGIC              WHEN DATEDIFF(source.l_shipdate,orders.o_orderdate) BETWEEN 0 AND 2 THEN "0-2 days"
-- MAGIC              WHEN DATEDIFF(source.l_shipdate,orders.o_orderdate) BETWEEN 2 AND 4 THEN "2-4 days"
-- MAGIC              ELSE "5+ days"
-- MAGIC           END
-- MAGIC ```
-- MAGIC
-- MAGIC The repeated code in this approach is an opportunity for error. Let's take a clearer, two-step approach.
-- MAGIC
-- MAGIC 1. In the metric-view definition editor, add this at the end of the **dimensions** section (taking care to indent properly).
-- MAGIC
-- MAGIC ```
-- MAGIC      - name: order_delay
-- MAGIC        expr: DATEDIFF(source.l_shipdate,orders.o_orderdate)
-- MAGIC        display_name: Order Delay in Days
-- MAGIC ```
-- MAGIC
-- MAGIC 2. Now add this new dimension immediately below the previous one (taking care to indent properly):
-- MAGIC
-- MAGIC ```
-- MAGIC     - name: order_delay_status
-- MAGIC       expr: |
-- MAGIC             CASE
-- MAGIC                WHEN order_delay < 0 THEN "Item Shipped Before Order!"
-- MAGIC                WHEN order_delay BETWEEN 0 AND 2 THEN "0-2 days"
-- MAGIC                WHEN order_delay BETWEEN 2 AND 4 THEN "2-4 days"
-- MAGIC                ELSE "5+ days"
-- MAGIC             END
-- MAGIC       display_name: Order Delay Status
-- MAGIC ```

-- MAGIC %md
-- MAGIC ## B2. Create a more sophisticated dimension
-- MAGIC
-- MAGIC Let's also make the contents of the raw data more transparent for human users. Notice that each order has an order status, which is either O for open, P for processing, or F for final. Let's replace those single-letter abbreviations with a word that fully declares the order status.
-- MAGIC
-- MAGIC 1. In the metric-view definition editor, add this at the end of the **dimensions** section (taking care to indent properly).
-- MAGIC
-- MAGIC ```
-- MAGIC   - name: Order Status
-- MAGIC     expr: |
-- MAGIC           CASE
-- MAGIC             WHEN orders.o_orderstatus = "O" THEN "Open"
-- MAGIC             WHEN orders.o_orderstatus = "P" THEN "Processing"
-- MAGIC             WHEN orders.o_orderstatus = "F" THEN "Final"
-- MAGIC             ELSE "Error"
-- MAGIC           END
-- MAGIC ```
-- MAGIC
-- MAGIC The pipe sign in the expression is one of YAML's ways to introduce a muilti-line string value, in this case a SQL CASE expression. We could put the whole expression on one line, but then it would be harder to read. 
-- MAGIC
-- MAGIC Notice that the case clause contains an ELSE so that the expression will never silently return a null value.

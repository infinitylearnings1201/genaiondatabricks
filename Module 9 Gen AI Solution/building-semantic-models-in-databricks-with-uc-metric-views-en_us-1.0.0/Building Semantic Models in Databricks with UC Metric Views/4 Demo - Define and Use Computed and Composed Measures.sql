-- MAGIC %md
-- MAGIC ## B1. Add a computed dimension
-- MAGIC
-- MAGIC Let's add a dimension for the year and quarter each order came in during. That way it'll be easier for data consumers to bucketize orders by quarter. We use YEAR() to extract a year number from a date, QUARTER() to give us a number from 1 to 4 based on the calendar quarter of a date, and CONCAT to string it all together, with a dash and a Q to separate them. So each order will have a quarter associated with it, consistently formatted like `1999-Q2`. 
-- MAGIC
-- MAGIC 1. In the metric-view definition editor, add this at the end of the **dimensions** section.
-- MAGIC
-- MAGIC
-- MAGIC ```
-- MAGIC - name: order_year_quarter
-- MAGIC   expr: CONCAT(YEAR(orders.o_orderdate), '-Q', QUARTER(orders.o_orderdate))
-- MAGIC ```
-- MAGIC
-- MAGIC Here we  clarify what table the o_orderdate field is in by prepending `orders` to it. This identifier works because **orders** was defined as a source in this YAML file. 
-- MAGIC
